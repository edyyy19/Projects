const addition = (a, b) => {
  return a + b;
};
console.log(`Addition result: ${addition(2, 3)}`);

//

const multiply = (a, b) => {
  return a * b;
};
console.log(`Addition result: ${multiply(2, 3)}`);

//

const division = (a, b) => {
  return a / b;
};
console.log(`Addition result: ${parseFloat(division(3, 3)).toFixed(2)}`);

//

const modulus = (a, b) => {
  return a % b;
};
console.log(`Addition result: ${modulus(2, 3)}`);

//

const exponential = (a, b) => {
  return a ** b;
};
console.log(`Addition result: ${exponential(2, 3)}`);

//

const subtraction = (a, b) => {
  return a - b;
};
console.log(`Addition result: ${subtraction(2, 3)}`);
